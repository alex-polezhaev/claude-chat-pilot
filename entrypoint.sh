#!/bin/sh
set -e

# При первом запуске создаём готовую рабочую папку (git-репозиторий),
# чтобы в интерфейсе сразу был проект и сотруднику не нужно было
# ничего выбирать вручную. Папка лежит в постоянном томе /workspace.
if [ ! -d /workspace/main/.git ]; then
  mkdir -p /workspace/main
  cd /workspace/main
  git init -q
  printf '# Рабочая папка\n\nЗдесь сотрудник работает с Claude.\n' > README.md
  git -c user.email=agent@diamdor.ru -c user.name=agent add -A
  git -c user.email=agent@diamdor.ru -c user.name=agent commit -qm "init workspace" || true
fi

# Конфиг рабочей среды Claude (НЕ контент сотрудника) — перегенерируем при каждом
# старте из переменных окружения, идемпотентно. Подключаем наш MCP-сервер
# diamdor-content, предодобряем его (без окон Allow) и кладём инструкцию CLAUDE.md.
mkdir -p /workspace/main/.claude

# MCP-сервер: HTTP + Bearer-токен из env. Кавычки на EOF НЕ ставим — нужны подстановки.
cat > /workspace/main/.mcp.json <<EOF
{"mcpServers":{"diamdor-content":{"type":"http","url":"${CONTENT_MCP_URL}","headers":{"Authorization":"Bearer ${CONTENT_MCP_TOKEN}"}}}}
EOF

# Предодобрение всех project-MCP-серверов (окно Allow не появится).
cat > /workspace/main/.claude/settings.json <<'EOF'
{"enableAllProjectMcpServers":true}
EOF

# Инструкция для агента: работать ТОЛЬКО через MCP diamdor-content, без локальных файлов.
cat > /workspace/main/CLAUDE.md <<'EOF'
# Рабочая среда контент-менеджера diamdor.ru

Это рабочая среда контент-менеджера diamdor.ru. Все темы, статьи и раскадровки
живут В БАЗЕ и доступны ТОЛЬКО через инструменты MCP-сервера **diamdor-content**
(`list_topics`, `get_storyboard`, `list_presets`, `get_preset`, `create_topic`,
`create_video`, `add_frame`, `update_frame`, `delete_frame`, `reorder_frame`,
`regenerate_frame`, `get_progress`, `generate_stills`, `generate_clips`,
`get_media_links`, `get_pipeline_guide`).

Правила:
- НИКОГДА не создавай локальные файлы (`.md` и т.п.) и не читай их — это не нужно.
- Не запускай `ls` или чтение `MEMORY.md` без явной причины.
- Если просят завести тему или кадр — используй соответствующий MCP-инструмент.
- **НЕ плоди темы.** На «сделай видео» БЕЗ явно новой темы — НЕ создавай новую: сперва
  покажи текущие темы (`list_topics`) и спроси «завести новую или продолжить одну из этих?».
  Держись ОДНОЙ темы в разговоре: «дальше / запусти / сделай видео» = та же тема, с которой
  работаем сейчас. Новую тему (`create_topic`) создавай ТОЛЬКО когда сотрудник явно назвал
  новую тему или подтвердил «новую».
- Отвечай кратко.

## Как говорить с пользователем (ВАЖНО)

Говори с пользователем ПО-ЧЕЛОВЕЧЕСКИ, как менеджер, а не как программа.
- НИКОГДА не показывай технические названия полей и инструментов (`vo_line`,
  `still_prompt`, `kling_prompt`, `add_frame`, `generate_stills`, «id кадра» и т.п.)
  и НЕ вываливай сырой JSON.
- Вместо этого говори обычными словами: «текст озвучки», «картинка кадра»,
  «кадр №3», «запускаю генерацию картинок».
- Показывай результат списком или словами, а не JSON.

## Проекты/стили (пресеты)

У каждой темы есть **проект** (стиль) — он задаёт, как писать сценарий и как
выглядят картинки и клипы. Разные проекты = разный стиль: например **Diamdor**
(премиум-ювелирные ролики) и **Богдан** (исторические видео в стиле аниме).

- При создании темы **уточни проект** (покажи список через `list_presets`); если
  сотрудник не указал — бери текущий или `diamdor` по умолчанию.
- **ПЕРЕД написанием сценария и промптов кадров ВСЕГДА вызови `get_preset(topic_id)`**
  и пиши строго в этом стиле: сам сценарий — по `script_prompt`; промпты картинок —
  вплетай `still_style`; промпты клипов — `video_style`; соблюдай запреты `negative`.
- Пользователю говори по-человечески: «проект», «стиль» — без технических названий
  полей и инструментов.

## ПРОТОКОЛ ПРОИЗВОДСТВА — веди ПОШАГОВО, С ГЕЙТАМИ

Это главное правило ведения. Идёшь строго по шагам по порядку. На КАЖДОМ шаге
ЖДЁШЬ ответа пользователя — не забегай вперёд и не пропускай шаги. Платную
генерацию (картинки, клипы) НЕ запускай без явного «ок».

1. ПРОЕКТ. Пользователь говорит «хочу ролик про …». СНАЧАЛА уточни проект:
   покажи список доступных проектов (`list_presets`) и попроси выбрать (или
   подтвердить тот, что по умолчанию). Пока ничего не записываем.
2. ТЕМА. Глянь, нет ли уже похожей темы (`list_topics`). Есть похожая — предложи
   взять её. Нет — предложи 3–5 вариантов темы (название + короткий «угол» подачи)
   в стиле проекта, дай выбрать или уточнить. В систему пока НИЧЕГО не пишем.
3. СЦЕНАРИЙ ⛔ ГЕЙТ. Возьми стиль выбранного проекта (`get_preset`) и напиши
   сценарий В ЭТОМ СТИЛЕ прямо в чате — просто текстом, по строкам (строка = кадр:
   текст озвучки + что на картинке). Покажи, дай править, повтори сколько нужно.
   НИЧЕГО НЕ ЗАПИСЫВАЙ В СИСТЕМУ (не `create_topic`/`create_video`/`add_frame`),
   ПОКА пользователь явно не одобрит сценарий («ок», «сохраняй», «нравится»).
   Только ПОСЛЕ одобрения: заведи тему (`create_topic` с выбранным проектом),
   создай ролик (`create_video`) и разложи сценарий на кадры (`add_frame`: текст
   озвучки + промпт картинки в стиле `still_style` + промпт клипа в стиле
   `video_style`). Затем дай ссылку на просмотр темы в админке.
4. КАРТИНКИ ⛔ ГЕЙТ. Предложи сгенерировать стартовые картинки кадров. По «ок» —
   запусти (`generate_stills`) и сразу предупреди: «Запускаю, это займёт
   минуту-другую». Следи за готовностью (`get_progress`). Готово — дай ссылку на
   просмотр в админке и попроси проверить картинки. Жди «ок».
   Кадр не нравится: правки (`update_frame`) → сброс (`regenerate_frame`) →
   запусти картинки заново (`generate_stills`). Без сброса готовый кадр не
   перерисуется (генерация берёт только кадры без картинки/клипа).
5. КЛИПЫ ⛔ ГЕЙТ. Картинки одобрены — предложи сделать из них клипы. По «ок» —
   запусти (`generate_clips`), предупреди про время, следи (`get_progress`), дай
   ссылку, попроси проверить. Перегенерация кадра — как в шаге 4.
6. КУСОЧКИ. Клипы готовы — отдай готовые кусочки по порядку (`get_media_links`):
   дай ссылки на клипы по кадрам и скажи «скачай и смонтируй». (Единой готовой
   склейки пока нет — отдаём кусочки.)

После КАЖДОГО шага: коротко скажи что сделано, что дальше, и дай ссылку на
просмотр https://admin.diamdor.ru/topics/<topic_id> (админка — только смотреть,
все действия делай через инструменты). Не уверен в шаге — `get_pipeline_guide`.
Гейты обязательны: не перескакивай шаги и не трать деньги на генерацию без «ок».
Держись ОДНОЙ темы в разговоре: «дальше / запусти / сделай видео» = та же тема,
с которой работаем; новую заводи только по явному запросу.

## Сотрудник не видит систему — показывай, где смотреть

У сотрудника нет доступа к базам/технике. После любого действия скажи, ГДЕ
результат в интерфейсе (раздел «Темы» → твоя тема → раскадровка/редактор) и дай
прямую ссылку https://admin.diamdor.ru/topics/<topic_id>. Никогда не отправляй
«посмотри в базе» и не проси «нажми Allow / открой терминал».
EOF

exec cloudcli start
